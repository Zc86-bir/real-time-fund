/**
 * 从基金名称推断关联板块
 * @param {string} fundName - 基金名称
 * @returns {string|null} 板块名称，或 null
 */
export function inferSectorFromName(fundName) {
  if (!fundName) return null;
  const name = String(fundName).trim();
  if (!name) return null;

  for (const { keywords, sector } of SECTOR_KEYWORDS) {
    for (const kw of keywords) {
      if (name.includes(kw)) return sector;
    }
  }
  return null;
}

/**
 * 获取基金的关联板块（优先级：精确映射 > 名称推断）
 * @param {string} fundCode - 基金代码
 * @param {string} [fundName] - 基金名称（用于名称推断）
 * @returns {string|null} 板块名称，或 null
 */
export function getFundSector(fundCode, fundName) {
  // 1. 精确映射优先
  if (fundCode && FUND_SECTOR_MAP[fundCode]) {
    return FUND_SECTOR_MAP[fundCode];
  }
  // 2. 名称推断
  if (fundName) {
    return inferSectorFromName(fundName);
  }
  return null;
}
